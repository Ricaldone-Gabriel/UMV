<h1>Capolavoro</h1>
<p>
    Lo scopo di questo capolavoro è la creazione di un Unmanned Marine Vehicle (UMV) per scopi di ricerca di base.
    <br>
    Questo veicolo è dotato di un termometro, igrometro e barometro. Il tutto è controllato da una scheda D1 R32 con
    ESP32 WROOM 32.
    <br>
    L'idea inizia durante l'estate del 2022 con l'obiettivo di creare un aereo radio telecomandato,
    durante la mia formaziona scolastica e personale ho ridotto l'obiettivo ad una nave radio telecomandata per
    sfruttare al meglio le mie conoscenze limitate e riuscire a completare il progetto.
</p>